let lista = []

// .length é uma das propriedades do array a qual retorna o tamanho em inteiro
console.log(lista.length)

// adiciona um elemento na lista
lista.push(5)
lista.push(8)

console.log(lista)


//push (adiciona elemento)
//map (percorre o arrazy)
//foreach ()

// adicionar um elemento no incio do array
lista.unshift(0)

console.log(lista)

lista.shift()

console.log(lista)

// remove o ultimo elemento, como sempre é o ultimo elemento, nao há necessidade de informar o parametro

lista.pop()

console.log(lista)

